# целое число в булевое значние
print(bool(0)) #False / Преобразовывая целые числа в логический тип мы всегда получим False преобразовывая 0
print(bool(2)) #True / и получи true при
print(bool(-2))#True / преобразовании любого другого целого числа 

# строку в булевое значение
print(bool('some text')) #True , поскольку любая не пустая строка это True
print(bool('')) #False, пустая строка это False 
