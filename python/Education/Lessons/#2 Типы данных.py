print(resultat) # Функция print() не возвращает результат выполнения функции в код, не хранит в себе данных, она лишь выводит на экран текст
# При отладке кода сначала будет выполнен print('Что-нибудь'), а затем возвращён resultat = None ,обе функции выполняются.
