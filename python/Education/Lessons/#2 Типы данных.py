# (4) Тип данных None/////////////////////////////////////////////////////////////////////////////////////////////////////
# Как говорилось ранее, функция print() не возвращает значение, а лишь выводит его на экран, но это не совсем точо
# Если быть точным то она возвращает ничего, то есть None
#
# Пример (1):
