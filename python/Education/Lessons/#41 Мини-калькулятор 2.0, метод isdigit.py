b = input('some number') # Вводим число b
if a.isdigit() and b.isdigit(): # метод isdigit проверяет, является ли переменная строкой, содержащей только цифры
    print('Сумма=' + str(int(a) + int(b)))       # isdigit вернёт true если введены только цифры
else:
    print('Ошибка')
